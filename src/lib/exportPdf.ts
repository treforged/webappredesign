function fmt(n: number): string {
  return new Intl.NumberFormat('en-US', { style: 'currency', currency: 'USD', maximumFractionDigits: 0 }).format(n);
}

export function exportForecastPdf(rows: ForecastRow[], period = 'All Time'): void {
  const exportDate = new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'short', day: 'numeric' });

  const htmlRows = rows.map(r => {
    const cashCls = r.endingCash < 0 ? 'neg' : '';
    const nwCls = r.netWorth < 0 ? 'neg' : 'pos';
    return `<tr>
      <td>${r.month}</td>
      <td class="num pos">${fmt(r.takeHome)}</td>
      <td class="num neg">−${fmt(r.totalExpenses)}</td>
      <td class="num neg">−${fmt(r.debtPayment)}</td>
      <td class="num ${cashCls}">${fmt(r.endingCash)}</td>
      <td class="num">${fmt(r.savingsBalance)}</td>
      <td class="num ${r.debtBalance > 0 ? 'neg' : ''}">${fmt(r.debtBalance)}</td>
      <td class="num ${nwCls}">${fmt(r.netWorth)}</td>
    </tr>`;
  }).join('');

  const html = `<!DOCTYPE html><html lang="en"><head><meta charset="utf-8"><title>Forged — Forecast</title><style>${PDF_STYLES}</style></head>
<body>
  <header>
    <div><h1>FORGED</h1><p class="meta">36-Month Forecast &middot; ${period}</p></div>
    <div class="right meta"><p>Exported ${exportDate}</p><p>${rows.length} month${rows.length !== 1 ? 's' : ''}</p></div>
  </header>
  <table>
    <thead><tr>
      <th>Month</th><th>Take-Home</th><th>Expenses</th><th>Debt Pmt</th><th>End Cash</th><th>Savings</th><th>Debt Bal</th><th>Net Worth</th>
    </tr></thead>
    <tbody>${htmlRows}</tbody>
  </table>
  <footer>Generated by Forged &middot; app.treforged.com</footer>
</body></html>`;

  openPrintWindow(html);
}

export function exportDashboardPdf(snap: DashboardSnapshot): void {
  const exportDate = new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'short', day: 'numeric' });

  const kpis = [
    { label: 'Liquid Cash', value: fmt(snap.liquidCash) },
    { label: 'Net Worth', value: fmt(snap.netWorth) },
    { label: 'Monthly Income', value: fmt(snap.income) },
    { label: 'Monthly Expenses', value: fmt(snap.expenses) },
    { label: 'Debt Payments', value: fmt(snap.totalDebtPayments) },
    { label: 'Savings Rate', value: `${snap.savingsRate.toFixed(1)}%` },
    { label: 'Total Saved', value: fmt(snap.totalSaved) },
    { label: 'CC Debt', value: fmt(snap.ccDebt) },
    { label: 'Cash Flow', value: fmt(snap.income - snap.expenses - snap.totalDebtPayments) },
  ];

  const kpiHtml = kpis.map(k => `<div class="kpi"><div class="kpi-label">${k.label}</div><div class="kpi-value">${k.value}</div></div>`).join('');

  const html = `<!DOCTYPE html><html lang="en"><head><meta charset="utf-8"><title>Forged — Dashboard Snapshot</title><style>${PDF_STYLES}</style></head>
<body>
  <header>
    <div><h1>FORGED</h1><p class="meta">Command Center Snapshot &middot; ${snap.month}</p></div>
    <div class="right meta"><p>Exported ${exportDate}</p></div>
  </header>
  <div class="kpi-grid">${kpiHtml}</div>
  <footer>Generated by Forged &middot; app.treforged.com</footer>
</body></html>`;

  openPrintWindow(html);
}
